# sol -> 이건 dfs로 했는데 맞는지 아직 모름

from collections import deque
n, m = map(int, input().split())

graph = []

for i in range(n):
    graph[i].append(list(map(int, input())))

answer = 0


def dfs(x, y):
    if x <= -1 or x >= n or y <= -1 or y >= n:
        return False

    if graph[x][y] == 1:
        answer += 1
        graph[x][y] = 0
        dfs(x-1, y)
        dfs(x+1, y)
        dfs(x, y-1)
        dfs(x, y+1)
        return True
    return False


for i in range(n):
    for j in range(m):
        dfs(i, j)

print(answer)


